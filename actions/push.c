#include "../push_swap.h"

void ft_pa(int *a, int *b, int ac)
{
	int i;
	
	i = ac - 1;
	while(i > 0)
	{
		a[i] = a[i - 1];
		i--;
	}
	a[0] = b[0];
	while(i < ac)
	{
		b[i] = b[i + 1];
		i++;
	}
	ft_printf("pa\n");
}